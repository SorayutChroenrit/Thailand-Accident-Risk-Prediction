// Create a mask polygon that covers the world except Thailand
export const createMaskPolygon = () => {
  // Outer ring - covers the whole world
  const outerRing: [number, number][] = [
    [-90, -180],
    [-90, 180],
    [90, 180],
    [90, -180],
    [-90, -180],
  ]

  // Inner ring - Thailand boundary (reversed to create hole)
  const innerRing = [...thailandBoundary].reverse()

  return {
    type: 'Feature' as const,
    properties: {},
    geometry: {
      type: 'Polygon' as const,
      coordinates: [
        outerRing.map(([lat, lng]) => [lng, lat]), // GeoJSON uses [lng, lat]
        innerRing.map(([lat, lng]) => [lng, lat]),
      ],
    },
  }
}
