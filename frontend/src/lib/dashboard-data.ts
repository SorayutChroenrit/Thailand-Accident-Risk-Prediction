// Get color for heatmap based on accident count (Red shades - light to dark)
export const getProvinceColor = (totalAccidents: number): string => {
  if (totalAccidents >= 10000) return "#991b1b"; // Very high - darkest red
  if (totalAccidents >= 5000) return "#b91c1c"; // High - dark red
  if (totalAccidents >= 3000) return "#dc2626"; // Medium-high - deep red
  if (totalAccidents >= 2000) return "#ef4444"; // Medium - red
  if (totalAccidents >= 1000) return "#f87171"; // Low-medium - bright red
  if (totalAccidents >= 500) return "#fca5a5"; // Low - light red
  return "#fecaca"; // Very low - lightest red/pink
};

// Get top provinces by accidents
export const getTopProvinces = (limit: number = 10) => {
  return [...provinceAccidentData]
    .sort((a, b) => b.totalAccidents - a.totalAccidents)
    .slice(0, limit);
};
